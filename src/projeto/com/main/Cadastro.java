    public void cadastrar(String nome) {
		String nomeRecebido;
                
		Funcionario funcionario = new Funcionario(nome);
		funcionarioDAO.salvar(funcionario);

		// Cadastro da digital
		try {
			DPFPFeatureExtraction featureExtractor = DPFPGlobal
					.getFeatureExtractionFactory().createFeatureExtraction();
			DPFPEnrollment enrollment = DPFPGlobal.getEnrollmentFactory()
					.createEnrollment();
			//4 leituras
			while (enrollment.getFeaturesNeeded() > 0) {
                                jTextAreaAvisosSistema.setText(jTextAreaAvisosSistema.getText() + "(Faltam " + enrollment.getFeaturesNeeded() + " leituras) \n");
                                
				DPFPSample sample = Biometria.getDigital();
				
                                if (sample == null)
					continue;

				DPFPFeatureSet featureSet;
				try {
					featureSet = featureExtractor.createFeatureSet(sample,
							DPFPDataPurpose.DATA_PURPOSE_ENROLLMENT);
				} catch (DPFPImageQualityException e) {
                                        JOptionPane.showMessageDialog(null, "Má qualidade: " + e.getCaptureFeedback().toString()+" Tente novamente. ");
					continue;
				}

				enrollment.addFeatures(featureSet);
			}

			funcionario.setDigital(enrollment.getTemplate());
			funcionarioDAO.salvar(funcionario); //atualiza no banco de dados ja com a digital

			JOptionPane.showMessageDialog(null,"Cadastrado com sucesso!");
		} catch (DPFPImageQualityException e) {
			JOptionPane.showMessageDialog(null,"Falha ao cadastrar digital.\n\n");
		} catch (InterruptedException e) {
			throw new RuntimeException(e);
		}

	}     
    
